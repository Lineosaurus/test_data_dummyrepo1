import Foundation

// Function to add two numbers
func add(_ a: Int, _ b: Int) -> Int {
    return a + b
}

// Variables
let num1 = 5
let num2 = 7

// Function call and output
let result = add(num1, num2)
print("Result:", result)

// Array example
let colors = ["red", "green", "blue"]
print("Colors:", colors)

// Dictionary example
let person = [
    "name": "Alice",
    "age": 30,
    "profession": "Engineer"
]
print("Person:", person)
